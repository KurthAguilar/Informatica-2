void coordinates(int x, int y){ //Funcion que busca obtener y buscar int que funcionaran como coordenadas
	HANDLE handle; // Tipo de Puntero inteligente que señala a algo dentro de la memoria

	handle = GetStdHandle(STD_OUTPUT_HANDLE); // Metodo Basico de Handle para obtener, escribir y hacer output de datos de memoria
	COORD pos; // Estipula el uso de coordenadas de tipo (0,0) para uso de posicion dentro del programa.
	pos.X = x;
	pos.Y = y;

	SetConsoleCursorPosition(handle, pos); //Recibe parametros para determinar posiciones del cursor

}

void hidecursor() { //Funcion simple de informacion de cursor, para que se oculte

	HANDLE handle;

	handle = GetStdHandle(STD_OUTPUT_HANDLE);
	CONSOLE_CURSOR_INFO cursor; // Determina y provee informacion del comportamiento del cursor que puede ser modificado

	cursor.dwSize = 3; // Tamaño cursor
	cursor.bVisible = false; //Visibilidad Cursor

	SetConsoleCursorInfo(handle, &cursor);
}

void color(int x) { //Funcion que simplifica el aplicar color a objetos
	SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), x);
}

